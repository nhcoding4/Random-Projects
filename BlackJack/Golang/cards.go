package main

import "fmt"

// --------------------------------------------------------------------------------------------------------------------

// Card struct and methods.

// --------------------------------------------------------------------------------------------------------------------

type Card struct {
	value int
	suit  string
	name  string
}

// ---------------------------------------------------------------------------------------------------------------------

func (c *Card) String() string {
	return fmt.Sprintf("%v %v", c.name, c.suit)
}

// --------------------------------------------------------------------------------------------------------------------

func (c *Card) convertAceLow() {
	const aceHighValue = 11
	const aceLowValue = 1

	if c.value == aceHighValue {
		c.value = aceLowValue
	}
}

